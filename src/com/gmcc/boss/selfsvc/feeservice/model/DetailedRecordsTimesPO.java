package com.gmcc.boss.selfsvc.feeservice.model;

import com.gmcc.boss.selfsvc.common.BasePO;

/**
 * 详单打印记录
 * 
 * 
 * @author g00140516
 * @version 1.0，2010-12-16
 * @see
 * @since
 */
public class DetailedRecordsTimesPO extends BasePO
{
    private static final long serialVersionUID = -9066580054390068166L;
    
    /**
     * 服务号码
     */
    private String servNumber = "";
    
    /**
     * 月份
     */
    private String month = "";
    
    /**
     * 已打印次数
     */
    private int times = 0;
    
    /**
     * 打印类型
     */
    private String listtype = "";
    
    // add begin g00140516 2013/02/01 R003C13L01n01 每年的12个月，每月详单均只能打印一次
    /**
     * 打印日期
     */
    private String printDate = "";
    // add end g00140516 2013/02/01 R003C13L01n01 每年的12个月，每月详单均只能打印一次
    
    public String getServNumber()
    {
        return servNumber;
    }
    
    public void setServNumber(String servNumber)
    {
        this.servNumber = servNumber;
    }
    
    public String getMonth()
    {
        return month;
    }
    
    public void setMonth(String month)
    {
        this.month = month;
    }
    
    public int getTimes()
    {
        return times;
    }
    
    public void setTimes(int times)
    {
        this.times = times;
    }

    public String getListtype()
    {
        return listtype;
    }

    public void setListtype(String listtype)
    {
        this.listtype = listtype;
    }

	public String getPrintDate()
	{
		return printDate;
	}

	public void setPrintDate(String printDate)
	{
		this.printDate = printDate;
	}


    
    
}
